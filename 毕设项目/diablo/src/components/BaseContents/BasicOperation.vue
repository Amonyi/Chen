                        <p>在攻击怪物时，你可以在画面上方看到怪物的名称，以及代表怪物生命值的红色长条，能够清楚看出怪物还能承受多少打击，然后才会剧烈爆炸死亡、或是倒在你脚下的那滩血水里。 当长条由满的（红色）变成空的（黑色），怪物就会死亡。（如果你看到怪物的上半身还在朝你爬过来、伸手要抓你的喉咙，那就表示它还没死透。） </p>
                        <p><strong>保持位置！</strong></p>      
                        <p>从战术上来说，站在当前的位置比冲进敌群更重要。特别是你是一名远程职业或尝试和盟友们在一起时候。按住 <span class="title3">Shift</span> 键可以让角色强制站立在原地，并施展技能或者发动基础攻击。但如果你是远程职业或在远处使用远程技能时，意外点击地面则可能让你的角色奔向该地。按住 <span class="title3">Shift</span> 键就可以避免了。</p>      
                        <p><strong>呼唤队友！</strong></p>
                        <p>游戏中按下<span class="title3">G</span>键就可以呼唤出战旗。此时在附近的其他队友都会在小地图上受到一个提示，以提醒他们你在这里发现了东西。同时，在村落中，你或你的队友可以通过战旗直接传送到你或你的队友身边，直接投入战斗和探索！多加利用战旗系统的功能，可以让组队游戏事半功倍。</p>
